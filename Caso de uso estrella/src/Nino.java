public class Nino {
    String nombre;
    String idTutor;

    public Nino() {
    }

    public Nino(String nombre, String idTutor) {
        this.nombre = nombre;
        this.idTutor = idTutor;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getIdTutor() {
        return idTutor;
    }

    public void setIdTutor(String idTutor) {
        this.idTutor = idTutor;
    }
    
    
}
